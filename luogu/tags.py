def _char_seq_match(input_s: str, tag_s: str) -> bool:
    """
    检查 input_s 的所有字符是否按顺序出现在 tag_s 中。
    专门处理中文缩写匹配，如：'动规' → '动态规划'。
    """
    i = 0
    for ch in tag_s:
        if i < len(input_s) and ch == input_s[i]:
            i += 1
    return i == len(input_s)


def _normalize_tag_text(text: str) -> str:
    """归一化标签文本，提升 `状压DP` / `状压 DP` 这类写法的匹配稳定性。"""
    return ''.join(ch.lower() for ch in text if ch.isalnum())


def fuzzy_match_tag(input_tag: str) -> list:
    """
    模糊匹配标签（搜索算法标签 + 来源标签）。

    Args:
        input_tag: 用户输入的标签关键词

    Returns:
        匹配到的标签列表（最多10个）
    """
    input_lower = input_tag.lower()
    input_compact = _normalize_tag_text(input_tag)
    matches = []
    seq_matches = []   # 字符顺序匹配（优先级低）

    all_tags = ALGORITHM_TAGS + list(SOURCE_TAG_IDS.keys())

    for tag in all_tags:
        # 完全匹配优先
        if input_tag == tag:
            return [tag]
        tag_lower = tag.lower()
        tag_compact = _normalize_tag_text(tag)
        # 大小写不敏感子串匹配
        if input_lower in tag_lower:
            matches.append(tag)
            continue
        # 忽略空格和符号后的子串/精确匹配
        if input_compact and (input_compact == tag_compact or input_compact in tag_compact):
            matches.append(tag)
            continue
        # 字符顺序匹配（支持中文缩写：动规 → 动态规划）
        if _char_seq_match(input_tag, tag):
            seq_matches.append(tag)

    return (matches + seq_matches)[:10]


